var path = require('path');
var jbase = require('jbase');

//----------------------------------------------------------------------------------------------------------------------

var db = { errors: jbase.errors };
var rootPath = path.join(__dirname, 'db');

//----------------------------------------------------------------------------------------------------------------------
// Site models
//----------------------------------------------------------------------------------------------------------------------

db.User = jbase.defineModel('users', {
    username: String,
    tagline: String,
    email: String,
    displayName: String,
    hash: String,
    salt: String,
    iterations: Number,
    created: { type: Date, default: Date.now() }
}, { rootPath: rootPath, pk: 'username' });

//----------------------------------------------------------------------------------------------------------------------

module.exports = db;

